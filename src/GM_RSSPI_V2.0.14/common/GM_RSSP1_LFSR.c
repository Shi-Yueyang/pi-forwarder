/**
* @brief FSFB_LFSR_LShift
*
* 快速左移运算.
* @param[in] This LFSR运算中间数据结构
* @return GM_RSSP1_BOOL
*/
static GM_RSSP1_BOOL GM_RSSP1_LFSR_LShift(GM_RSSP1_LFSR* This)
{
    GM_RSSP1_INT32 i = 0;
    GM_RSSP1_UINT32 nTmp = 0U;

    if ((NULL == This) || (This->leftTable == NULL))
    {
        return GM_RSSP1_FALSE;
    }

    for (i = 0; i < 4; ++i)
    {
        nTmp = ((This->reg) & 0xFF000000U) >> 24U;  // 提取reg的最高8位
        This->reg <<= 8U;   // 将reg左移8位，腾出低位空间，最高8位被顶出
        This->reg ^= This->leftTable[nTmp]; // 使用nTmp作为索引从leftTable中获取反馈值，并与reg的最低8位异或
    }

    return GM_RSSP1_TRUE;
}

/**
* @brief FSFB_LFSR_RShift
*
* 快速右移运算.
* @param[in] This LFSR运算中间数据结构
* @return GM_RSSP1_BOOL
*/
static GM_RSSP1_BOOL GM_RSSP1_LFSR_RShift(GM_RSSP1_LFSR* This)
{
    GM_RSSP1_INT32 i = 0;
    GM_RSSP1_UINT32 nTmp = 0U;

    if ((NULL == This) || (This->rightTable == NULL))
    {
        return GM_RSSP1_FALSE;
    }

    for (i = 0; i < 4; ++i)
    {
        nTmp = (This->reg) & 0xFFU;
        This->reg >>= 8U;
        This->reg ^= This->rightTable[nTmp];
    }

    return GM_RSSP1_TRUE;
}

GM_RSSP1_BOOL GM_RSSP1_LFSR_Init(GM_RSSP1_LFSR* This, const GM_RSSP1_UINT32* pLeftTable, const GM_RSSP1_UINT32* pRightTable)
{
    if ((NULL == This) || (NULL == pLeftTable) || (NULL == pRightTable))
    {
        return GM_RSSP1_FALSE;
    }

    This->leftTable  = pLeftTable;

    This->rightTable = pRightTable;
    This->reg        = 0U;

    return GM_RSSP1_TRUE;
}

GM_RSSP1_BOOL GM_RSSP1_LFSR_Load(GM_RSSP1_LFSR* This, GM_RSSP1_UINT32 data)
{
    if (NULL == This)
    {
        return GM_RSSP1_FALSE;
    }

    This->reg = data;

    return GM_RSSP1_TRUE;
}

GM_RSSP1_BOOL GM_RSSP1_LFSR_Read(GM_RSSP1_LFSR* This, GM_RSSP1_UINT32* pData)
{
    if ((NULL == This) || (NULL == pData))
    {
        return GM_RSSP1_FALSE;
    }

    *pData = This->reg;

    return GM_RSSP1_TRUE;
}

GM_RSSP1_BOOL GM_RSSP1_LFSR_Add(GM_RSSP1_LFSR* This, GM_RSSP1_UINT32 data)
{
    GM_RSSP1_BOOL rt = GM_RSSP1_FALSE;

    if (NULL == This)
    {
        return GM_RSSP1_FALSE;
    }

    /** @brief 先异或 */
    This->reg ^= data;

    /** @brief 再左移 */
    rt = GM_RSSP1_LFSR_LShift(This);

    return rt;
}

GM_RSSP1_BOOL GM_RSSP1_LFSR_Post(GM_RSSP1_LFSR* This, GM_RSSP1_UINT32 data)
{
    GM_RSSP1_UINT32 temp = 0U;
    GM_RSSP1_BOOL rt     = GM_RSSP1_FALSE;

    if (NULL == This)
    {
        return GM_RSSP1_FALSE;
    }

    temp = This->reg;

    This->reg = data;

    rt = GM_RSSP1_LFSR_RShift(This);

    if (GM_RSSP1_FALSE == rt)
    {
        return GM_RSSP1_FALSE;
    }

    This->reg ^= temp;

    return GM_RSSP1_TRUE;
}

